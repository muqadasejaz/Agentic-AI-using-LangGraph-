# ⚙️ FIRST WORKFLOW — BMI CALCULATOR
# ============================================================

# Workflow Inputs:
# - Height (meters)
# - Weight (kg)

# Formula:
# BMI = weight / (height ** 2)

# ------------------------------------------------------------
# 🔹 State Definition
# ------------------------------------------------------------

# State contains:
# - weight: float
# - height: float
# - bmi: float

# Implemented using TypedDict


# ------------------------------------------------------------
# 🔹 Graph Construction Steps
# ------------------------------------------------------------

# 1. Create StateGraph with BMI state
# 2. Add node: calculate_bmi
# 3. Define edges:
#    start → calculate_bmi → end
# 4. Compile graph
# 5. Execute graph


# ------------------------------------------------------------
# 🔹 Node Function Logic
# ------------------------------------------------------------

# - Extract height and weight from state
# - Calculate BMI
# - Update state with BMI
# - Return updated state


# ------------------------------------------------------------
# 🔹 Execution
# ------------------------------------------------------------

# - Pass input:
#   {"weight": value, "height": value}
# - Output:
#   Updated state with BMI


