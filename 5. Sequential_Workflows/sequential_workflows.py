# 🔗 PROMPT CHAINING WORKFLOW
# ============================================================

# Concept:
# - Multiple sequential LLM calls

# Example Workflow:
# 1. Generate outline
# 2. Generate blog using outline

# Flow:
# start → create_outline → create_blog → end


# ------------------------------------------------------------
# 🔹 State Definition
# ------------------------------------------------------------

# - title: string
# - outline: string
# - content: string


# ------------------------------------------------------------
# 🔹 Nodes
# ------------------------------------------------------------

# 1. create_outline:
#    - Generates blog outline using LLM

# 2. create_blog:
#    - Generates full blog using title + outline


# ------------------------------------------------------------
# 🔹 Key Benefit
# ------------------------------------------------------------

# - State evolves step-by-step
# - Intermediate outputs (outline) are accessible
# - More control compared to simple chains


